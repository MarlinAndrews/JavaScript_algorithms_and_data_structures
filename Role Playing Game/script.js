function fightDragon() {
  fighting = 2;
  goFight();
}

// Function to prepare for a fight, update UI, and set monster stats
function goFight() {
  update(locations[3]);
  monsterHealth = monsters[fighting].health;
  monsterStats.style.display = "block";
  monsterName.innerText = monsters[fighting].name;
  monsterHealthText.innerText = monsterHealth;
}
// Function to handle player's attack during a fight
function attack() {
   // Display attack details in the narrative text
  text.innerText = "The " + monsters[fighting].name + " attacks.";
  text.innerText += " You attack it with your " + weapons[currentWeapon].name + ".";
  // Reduce player's health based on the monster's attack value
  health -= getMonsterAttackValue(monsters[fighting].level);
  if (isMonsterHit()) {
    monsterHealth -= weapons[currentWeapon].power + Math.floor(Math.random() * xp) + 1;    
  } else {
    // Display a message if the player misses the attack
    text.innerText += " You miss.";
  }
   // Update player's and monster's health in the UI
  healthText.innerText = health;
  monsterHealthText.innerText = monsterHealth;
  if (health <= 0) {
    lose();
  } else if (monsterHealth <= 0) {
    // Determine whether it's a dragon fight or another monster, and call the appropriate function
    fighting === 2 ? winGame() : defeatMonster();
  }
   // Random chance for the player's weapon to break
  if (Math.random() <= .1 && inventory.length !== 1) {
    text.innerText += " Your " + inventory.pop() + " breaks.";
    currentWeapon--;
  }
}
// Function to calculate the monster's attack value
function getMonsterAttackValue(level) {
  const hit = (level * 5) - (Math.floor(Math.random() * xp));
  console.log(hit);
  return hit > 0 ? hit : 0;
}
// Function to determine if the player's attack hits the monster
function isMonsterHit() {
  return Math.random() > .2 || health < 20;
}

// Function to handle player's dodge during a fight
function dodge() {
  text.innerText = "You dodge the attack from the " + monsters[fighting].name;
}

// Function to handle victory over a monster, update player's stats, and transition to the next location
function defeatMonster() {
  gold += Math.floor(monsters[fighting].level * 6.7);
  xp += monsters[fighting].level;
  goldText.innerText = gold;
  xpText.innerText = xp;
  update(locations[4]);
}

function lose() {
  update(locations[5]);
}

function winGame() {
  update(locations[6]);
}
// Function to restart the game, resetting player stats and transitioning to the town location
function restart() {
  xp = 0;
  health = 100;
  gold = 50;
  currentWeapon = 0;
  inventory = ["stick"];
  goldText.innerText = gold;
  healthText.innerText = health;
  xpText.innerText = xp;
  goTown();
}

// Function to handle easter egg event, updating UI and transitioning to the easter egg location
function easterEgg() {
  update(locations[7]);
}
// Function to initiate easter egg with the number 2
function pickTwo() {
  pick(2);
}
// Function to initiate easter egg with the number 8
function pickEight() {
  pick(8);
}
// Main function for the easter egg game, comparing the player's pick with random numbers
function pick(guess) {
  const numbers = [];
  // Generate an array of 10 random numbers between 0 and 10
  while (numbers.length < 10) {
    numbers.push(Math.floor(Math.random() * 11));
  }
  // Display the player's pick and the generated random numbers
  text.innerText = "You picked " + guess + ". Here are the random numbers:\n";
  for (let i = 0; i < 10; i++) {
    text.innerText += numbers[i] + "\n";
  }
  // Check if the player's pick matches any of the random numbers
  if (numbers.includes(guess)) {
    text.innerText += "Right! You win 20 gold!";
    gold += 20;
    goldText.innerText = gold;
  } else {
    text.innerText += "Wrong! You lose 10 health!";
    health -= 10;
    healthText.innerText = health;
    if (health <= 0) {
      lose();
    }
  }
}